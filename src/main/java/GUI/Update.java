   void Display(String Email) {
    String filePath = "F:\\NetBeans 19\\Projects\\UPDATEDREG-main\\GUILOGIN\\DATA\\" + Email + ".txt";
    Path path = Paths.get(filePath);
    NEmail = Email;
    if (Files.exists(path)) {
        try {
            List<String> lines = Files.readAllLines(path);
            for (int i = 0; i < lines.size(); i++) {
                String line = lines.get(i);
                txtFname.setText(lines.get(0));
                txtEmail.setText(lines.get(4));
                txtPass.setText(lines.get(5));
                txtCPass.setText(lines.get(6));

                if (i == 2) {
                    // Assuming that cboxCourse and cboxYear are your JComboBoxes
                    cboxC.removeAllItems();
                    cboxY.removeAllItems();

                    // Split the combined course and year
                    String[] parts = line.split("-");
                    if (parts.length == 2) {
                        // Add predefined course choices to cboxCourse
                        String[] courseChoices = {"BSIT", "BSBA", "BSOA", "BSCRIM","BSED"};
                        for (String course : courseChoices) {
                            cboxC.addItem(course);
                        }

                        // Add predefined year choices to cboxYear
                        String[] yearChoices = {"1st year", "2nd year", "3rd year", "4th year"};
                        for (String year : yearChoices) {
                            cboxY.addItem(year);
                        }

                        // Set the selected items in the combo boxes
                        cboxC.setSelectedItem(parts[0]);
                        cboxY.setSelectedItem(parts[1]);
                    }
                }

               
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
}
        
