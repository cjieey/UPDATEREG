    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
         if (!txtCPass.getText().equals(txtPass.getText())) {
        txtCPass.setBackground(Color.red);
        lblAlert.setText("Confirm password not match");
    } else {
        String Fname = txtFname.getText();
        String NewEmail = txtEmail.getText();
        String old = NEmail;
        String Pass = txtPass.getText();
        String CPass = txtCPass.getText();
        String Course = (String) cboxC.getSelectedItem() + "-" + (String) cboxY.getSelectedItem();
        String oldFilePath = "F:\\NetBeans 19\\Projects\\UPDATEDREG-main\\GUILOGIN\\DATA\\" + NEmail + ".txt";
        String newFilePath = "F:\\NetBeans 19\\Projects\\UPDATEDREG-main\\GUILOGIN\\DATA\\" + txtEmail.getText() + ".txt";

        if (Files.exists(Paths.get(oldFilePath))) {
            try {
                List<String> lines = Files.readAllLines(Paths.get(oldFilePath));

                lines.set(0, Fname);
                lines.set(4, txtEmail.getText());
                lines.set(5, Pass);
                lines.set(6, CPass);
                lines.set(2, Course);

                Files.write(Paths.get(oldFilePath), lines);

                
                Files.move(Paths.get(oldFilePath), Paths.get(newFilePath), StandardCopyOption.REPLACE_EXISTING);

                Updatedone x = new Updatedone();
                x.show();
                this.hide();
            } catch (IOException e) {
                e.printStackTrace();
            }
        } else {
            lblAlert.setText("Email does not exist");
        }
    }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

